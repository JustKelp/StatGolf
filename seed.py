def seed():
    from models import init_db
    init_db()

    con = sqlite3.connect(DB_PATH)

    inserted = 0
    skipped = 0
    for row in STAT_VALUES:
        try:
            con.execute(
                "INSERT INTO sg_stat_values (player_name, player_id, sport, stat_category, season_year, value) "
                "VALUES (?,?,?,?,?,?)",
                row
            )
            inserted += 1
        except sqlite3.IntegrityError:
            skipped += 1

    for hole in SAMPLE_PUZZLE:
        con.execute(
            """INSERT OR REPLACE INTO sg_puzzle
                   (puzzle_date, hole_number, target_distance, par, club_bag, hazard_bands, published, created_by)
               VALUES (?,?,?,?,?,?,1,'seed')""",
            (
                SAMPLE_PUZZLE_DATE,
                hole["hole_number"],
                hole["target_distance"],
                hole["par"],
                json.dumps(hole["club_bag"]),
                json.dumps(hole["hazard_bands"]),
            )
        )

    con.commit()
    con.close()

    # Self-heal: merge any seed player_id that collides by name with a scraped ID
    # (guards against hand-typed IDs that don't match Sports-Reference, e.g. SmiiBr00).
    from models import dedupe_players
    merged, moved, deleted = dedupe_players()

    print(f"Seeded {inserted} stat rows ({skipped} already existed), puzzle for {SAMPLE_PUZZLE_DATE}.")
    if merged:
        print(f"Auto-dedup: merged {merged} duplicate player ID(s) — {moved} rows moved, {deleted} removed.")


if __name__ == "__main__":
    seed()
